const AnimalListNode = document.getElementById('allatok');
const AnimalTypeNode = document.getElementById('faj');
AnimalListNode.addEventListener('change', function() {
	stinj();
});

// b
const Button_Pet = document.getElementById('button_pet');
const Button_Benefit = document.getElementById('button_benefit');
const Button_Ornament = document.getElementById('button_ornament');
const Button_Other = document.getElementById('button_other');

Button_Pet.addEventListener('click', function() {
	FillAnimalTypeList('Pet');
	stinj();
});
Button_Benefit.addEventListener('click', function() {
	FillAnimalTypeList('Benefit');
	stinj();
});
Button_Ornament.addEventListener('click', function() {
	FillAnimalTypeList('Ornament');
	stinj();
});
Button_Other.addEventListener('click', function() {
	Unavailable();
});
function FillAnimalTypeList(Content)
{
	AnimalListNode.setAttribute('type', Content);

	ClearList(AnimalListNode);
	let array = Object.keys(Animal[Content]);
	for(let i = 0; i < array.length; ++i)
	{
		let option = new Option(array[i], array[i]);
		AnimalListNode.appendChild(option);
	}
}
function ClearList(Node)
{
	for(let i = Node.childElementCount - 1; i >= 0; --i)
		Node.children[i].remove();
}
function stinj()
{
	ClearList(AnimalTypeNode);
	if(AnimalListNode.getAttribute('type') !== null && AnimalListNode.value !== '')
	{
			Animal[AnimalListNode.getAttribute('type')][AnimalListNode.value].forEach((element) => {
			let option = new Option(element, element);
			AnimalTypeNode.appendChild(option);
		});
	}
}
function Unavailable()
{
	ClearList(AnimalListNode);
	ClearList(AnimalTypeNode);
	AnimalListNode.setAttribute('type', 'null');
	AnimalListNode.appendChild(new Option('Jelenleg nincs egyéb kategoria!', 'null'));
	AnimalTypeNode.appendChild(new Option('Jelenleg nincs egyéb kategoria!', 'null'));

}